    private void jButtonProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcurarActionPerformed
        EmprestimoController emprestimoController = new EmprestimoController();
        MaterialBibliograficoController materialController = new MaterialBibliograficoController();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        
        Emprestimo emprestimo = new Emprestimo();
        List<Emprestimo> listaEmprestimo = emprestimoController.consultarEmprestimo(Integer.parseInt(jTextFieldID.getText()), Integer.parseInt(jTextFieldIDUsuario.getText()), emprestimo);
        if (listaEmprestimo.isEmpty()) {
            NaoEncontrado naoEncontrado = new NaoEncontrado();
            naoEncontrado.setVisible(true);
        } else {
            switch (opcao){
                case 0:  //Devolução
                    Emprestimo emp = listaEmprestimo.get(0);
                    emprestimoController.enviarDevolucao(emp);

                    switch (emp.getTipoMaterial()) {
                        case 0:
                            Livro livro = new Livro();
                            List<Livro> listaLivro = materialController.consultarMaterialID(0, emp.getIdMaterial(), livro);

                            if (listaLivro.isEmpty()) {
                                NaoEncontrado naoEncontrado = new NaoEncontrado();
                                naoEncontrado.setVisible(true);
                            } else {
                                listaLivro.get(0).setQuantLocados(listaLivro.get(0).getQuantLocados() - 1);
                                materialController.editarLivro(listaLivro.get(0));
                            }
                            break;

                        case 1:
                            Artigo artigo = new Artigo();
                            List<Artigo> listaArtigo = materialController.consultarMaterialID(0, emp.getIdMaterial(), artigo);

                            if (listaArtigo.isEmpty()) {
                                NaoEncontrado naoEncontrado = new NaoEncontrado();
                                naoEncontrado.setVisible(true);
                            } else {
                                listaArtigo.get(0).setQuantLocados(listaArtigo.get(0).getQuantLocados() - 1);
                                materialController.editarArtigo(listaArtigo.get(0));
                            }
                            break;

                        case 2:
                            Periodico periodico = new Periodico();
                            List<Periodico> listaPeriodico = materialController.consultarMaterialID(0, emp.getIdMaterial(), periodico);

                            if (listaPeriodico.isEmpty()) {
                                NaoEncontrado naoEncontrado = new NaoEncontrado();
                                naoEncontrado.setVisible(true);
                            } else {
                                listaPeriodico.get(0).setQuantLocados(listaPeriodico.get(0).getQuantLocados() - 1);
                                materialController.editarPeriodico(listaPeriodico.get(0));
                            }
                            break;

                        case 3:
                            Video video = new Video();
                            List<Video> listaVideo = materialController.consultarMaterialID(0, emp.getIdMaterial(), video);

                            if (listaVideo.isEmpty()) {
                                NaoEncontrado naoEncontrado = new NaoEncontrado();
                                naoEncontrado.setVisible(true);
                            } else {
                                listaVideo.get(0).setQuantLocados(listaVideo.get(0).getQuantLocados() - 1);
                                materialController.editarVideo(listaVideo.get(0));
                            }
                            break;
                    }
                break;
                
                case 1: //Renovação
                    Calendar novaData = Calendar.getInstance();
                    novaData.add(Calendar.DAY_OF_MONTH, 7);
                    listaEmprestimo.get(0).setDataDevolucao(novaData.getTime());
                    emprestimoController.enviarDevolucao(listaEmprestimo.get(0));
                break;
            }
        
            ConfirmaOperacao confirma = new ConfirmaOperacao();
            confirma.setVisible(true);
            dispose();    
        }
    }//GEN-LAST:event_jButtonProcurarActionPerformed

